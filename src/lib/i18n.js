export const useLang = create((set, get) => ({
  lang: localStorage.getItem('rr_lang') || 'uz',
  setLang(lang) {
    localStorage.setItem('rr_lang', lang);
    set({ lang });
  },
  t(key) {
    const v = translations[get().lang]?.[key];
    if (v !== undefined) return v;
    const en = translations.en[key];
    return en !== undefined ? en : key;
  },
}));

export function useT() {
  const lang = useLang((s) => s.lang);
  return (key) => {
    const v = translations[lang]?.[key];
    if (v !== undefined) return v;
    const en = translations.en[key];
    return en !== undefined ? en : key;
  };
}
